public class Exuation3 {

	public static void main(String[] args) {
		int x=30;
		int y=20;
		
		double z;
		
		z = ( Math.sin(x) + Math.cos(y) ) / ( Math.cos(x) - Math.sin(y) ) * Math.tan(x)*y ;
		System.out.println ("x = " + x);
		System.out.println ("y = " + y);
		
		System.out.println ("z = " + z);

	}

}
